package top.wangyushuang.Data;

public class User {
	private String no1;
	private String no2;
	private String no3;
	

	public String getNo1() {
		return no1;
	}


	public void setNo1(String no1) {
		this.no1 = no1;
	}


	public String getNo2() {
		return no2;
	}


	public void setNo2(String no2) {
		this.no2 = no2;
	}


	public String getNo3() {
		return no3;
	}


	public void setNo3(String no3) {
		this.no3 = no3;
	}


	public String toString() {
		return no1+":"+no2+":"+no3;
	}
}
